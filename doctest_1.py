def add(a,b):
    """function to add two numbers
    usage examples-
    >>> add(2,9)
    11.0
    >>> add(100.1,9.2)
    109.3
    """
    return float(a + b)

def square(x,y):
    """function to square a number
    examples:-
    >>> square(3,2)
    9
    >>> square(5,3)
    125.0
    """
    return int(x**y)